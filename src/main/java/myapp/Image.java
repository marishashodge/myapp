package myapp;

public class Image {

    private String imageName;
    private String imageUri;

    public String getName() {
        return imageName;
    }

    public void setName(String name) {
        this.imageName = name;
    }

    public String getImageUri() {
        return imageUri;
    }

    public void setImageUri(String imageUri) {
        this.imageUri = imageUri;
    }

}
